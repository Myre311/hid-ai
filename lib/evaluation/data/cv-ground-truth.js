export function getCvTrackingSetForSession(sessionId) {
  if (!sessionId) sessionId = "default-fallback-seed";
  const seed = _hash(sessionId);
  const bgUrl = TRACKING_BACKGROUNDS[seed % TRACKING_BACKGROUNDS.length];
  const traj = TRAJECTORIES[(seed >>> 8) % TRAJECTORIES.length];
  const occluder = OCCLUDER_PRESETS[(seed >>> 12) % OCCLUDER_PRESETS.length];
  const total = 50;
  const pw = 0.06, ph = 0.22;
  const frames = Array.from({ length: total }, (_, i) => {
    const t = i / (total - 1);
    const px = traj.start.x + (traj.end.x - traj.start.x) * t;
    const py = traj.start.y + (traj.end.y - traj.start.y) * t;
    const inRange = (r) => i >= r[0] && i <= r[1];
    const isOcclusion = inRange(occluder.range) || inRange(occluder.range2);
    const phase = (i % 4) / 4;
    const stride = pw * 0.18 * Math.sin(phase * Math.PI * 2);
    return {
      frameNumber: i + 1,
      imagePath: bgUrl,
      personOverlay: { x: px, y: py, w: pw, h: ph, stride },
      occluderOverlay: isOcclusion ? { type: occluder.type, x: px, y: py + ph * 0.5 } : null,
      expectedBbox: { x: px, y: py, w: pw, h: ph },
      isOcclusion,
      occlusionType: isOcclusion ? occluder.type : null,
    };
  });
  return {
    videoContext: `Surveillance urbaine — trajectoire ${traj.name}, occluder ${occluder.type}`,
    targetObjectId: "person-1",
    backgroundUrl: bgUrl,
    frames,
  };
}

